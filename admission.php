								<h2>Requirement</h2>
								<h3>Secondary Education</h3>
								Applicants should be attaining their high school degree before July 1, 2018 for 2018 Fall Entry. Either AP, A-Level, IB, or 高考 is accepted. There is <strong>NO</strong> specific requirement for courses while the <strong>Maximum Requirement of Weighted GPA is 3.99</strong>. Applicants with unqualified GPA in their final report will be dismissed and will not refunded application fee.
								<br><br>
								<h3>Standard Test</h3>
								For applicants taking the redesigned SAT, the <strong>maximum</strong> score requirement is <strong>1,500</strong>. For ACT, the <strong>maximum</strong> score requirement is <strong>33</strong>. Please contact the Office of Admissions for inquiries of other standard tests.
								Overqualified applicants will have an unfair advantages over other candidates and thus will <strong>NOT</strong> be accepted.
								<br><br>
								<h3>English Proficiency</h3>
								While all students are required to take either TOEFL or IELTS test, the <strong>maximum</strong> TOEFL score requirement is <strong>110</strong> and the <strong>IELTS score maximum</strong> score requirement is <strong>8</strong>. Overqualified applicants will have an unfair advantages over other candidates and thus will <strong>NOT</strong> be accepted.
								<br><br><br>
								<h2>Admission Schemes</h2>
								<h3>Early Death (ED)</h3>
								Applicants who submit their application by <strong>0:00 (Eastern Standard Time) November 1, 2017 </strong> will be considered as ED. ED applicants will receive their rejection letters earlier than RD applicants.
								<br><br>
								<h3>Regular Death (RD)</h3>
								
								Applicants who wish to receive their rejection letter normally should choose RD and submit their application by <strong>0:00 (Eastern Standard Time) January 1, 2018 </strong> .
								<br><br><br>
								<h2>Procedures</h2>
								<h3>The Uncommon Application</h3>
								Applicants can submit their application through the <strong><a href="https://www.commonapp.org">Uncommon Application</a></strong> website.
